<p>The most powerful shortcut in NCLEX pharmacology is learning drug name suffixes. Once you recognize a suffix, you immediately know the drug class, mechanism, and likely nursing concerns — even for drugs you've never seen before. This is not a trick; it's how pharmaceutical naming conventions actually work.</p>

